def test_get_function(api_get_event):
    ret = app_get.lambda_handler(api_get_event, "")
    data = ret["body"]

    assert ret["statusCode"] == 200
    assert data > 0

def test_put_function(api_post_event):
    get_ret = app_get.lambda_handler(api_get_event, "")
    currentValue = int(get_ret["body"])

    ret = app_put.lambda_handler(api_post_event, "")
    data = int(ret["body"])

    assert ret["statusCode"] == 200
    assert data == currentValue + 1
